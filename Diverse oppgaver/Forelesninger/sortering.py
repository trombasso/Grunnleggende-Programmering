import time


def timer(func):
    def wrapper(*args, **kwargs):
        a = time.time()
        func(*args, **kwargs)
        print(f"Time used: {time.time() - a:.8f}")
        return func(*args, **kwargs)

    return wrapper


@timer
def insert_sort(lst):
    for i in range(1, len(lst)):
        current_element = lst[i]
        k = i - 1
        while current_element < lst[k] and lst[k] >= 0:
            lst[k + 1] = lst[k]
            k -= 1
        lst[k + 1] = current_element

    return lst


@timer
def bubblesort(lst):
    k = len(lst)
    while k > 0:
        for i in range(k - 1):
            if lst[i] > lst[i + 1]:
                lst[i + 1], lst[i] = lst[i], lst[i + 1]
        k -= 1
    return lst


@timer
def bubblesort_2(lst):
    k = len(lst)
    need_new_scan = True
    while k > 0 and need_new_scan:
        need_new_scan = False
        for i in range(k - 1):
            if lst[i] > lst[i + 1]:
                lst[i + 1], lst[i] = lst[i], lst[i + 1]
                need_new_scan = True
        k -= 1
    return lst


