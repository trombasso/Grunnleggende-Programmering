def quicksort(lst, left, right):
    if left >= right:
        return lst

    pivot = lst[(left + right) // 2]

    partition_index = partition(lst, left, right, pivot)
    quicksort(lst, left, partition_index - 1)
    quicksort(lst, partition_index, right)

    return lst


def partition(lst, left, right, pivot):
    while right >= left:

        while lst[left] < pivot:
            left += 1

        while lst[right] > pivot:
            right -= 1

        if right >= left:
            lst[right], lst[left] = lst[left], lst[right]
            left += 1
            right -= 1

    return left


class Heapsort:
    def __init__(self):
        self.lst = []

    def add(self, element):
        self.lst.append(element)
        current = len(self.lst) - 1
        parent = (current - 1) // 2
        while self.lst[current] > self.lst[parent]:
            self.lst[current], self.lst[parent] = self.lst[parent], self.lst[current]
            current = parent
            parent = (current - 1) // 2

    def remove_root(self):
        # self.lst = len(self.lst) - 1
        self.lst[0], self.lst[-1] = self.lst[-1], self.lst[0]
        removed_item = self.lst.pop()
        current = 0
        if len(self.lst) == 0:
            return removed_item

        while current < len(self.lst):
            left_child = 2 * current + 1
            right_child = 2 * current + 2

            if left_child >= len(self.lst):
                break

            left_will_swap = False
            will_swap = False

            if right_child == len(self.lst):  # Case is that only left child and endoflst
                if self.lst[current] < self.lst[left_child]:
                    left_will_swap = True
                    will_swap = True

            else:
                if self.lst[left_child] > self.lst[right_child]:
                    left_will_swap = True
                    will_swap = True
                    if self.lst[right_child] > self.lst[left_child]:
                        left_will_swap = False
                        left_will_swap = True

            if will_swap:
                index = left_child if left_will_swap else right_child
                self.st[current], self.lst[index] = self.lst[index], self.lst[current]
                current = index
            else:
                break

        return removed_item


def heap(lst):
    heap = Heapsort()
    for item in lst:
        heap.add(item)

    for i in range(len(lst) - 1, -1, -1):
        lst[i] = heap.remove_root()

    return lst


