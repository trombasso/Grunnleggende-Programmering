def merge_sort(lst):
    if len(lst) > 1:
        first_half = lst[: len(lst) // 2]
        second_half = lst[len(lst) // 2 :]

        merge_sort(first_half)
        merge_sort(second_half)

        merge(first_half, second_half, lst)

    return lst


def merge(first, second, target):
    current1 = 0
    current2 = 0
    current3 = 0

    while current3 < len(target):

        if first[current1] < second[current2]:
            target[current3] = first[current1]
            current1 += 1
        else:
            target[current3] = second[current2]
            current2 += 1
        current3 += 1

        if current1 == len(first) or current2 == len(second):
            break

        while current1 < len(first):
            target[current3] = first[current1]
            current1 += 1
            current3 += 1

        while current2 < len(second):
            target[current3] = second[current2]
            current2 += 1
            current3 += 1

        return target


